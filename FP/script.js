function check(form){
  if(form.username.value == ""){
    alert("Please enter a username")
  }
  if(form.password.value == ""){
    alert("Please enter a password")
  }
  if(form.fname.value == ""){
    alert("Please enter your first name")
  }
  if(form.lname.value == ""){
    alert("Please enter your last name")
  }
}